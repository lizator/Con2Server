    <h4>Generelt</h4>
    <p>Ja, undskyld vi bliver en smule philosofiske, men vi føler nu det er et vigtigt spørgsmål at besvare.</p>

    <p><b>Con2</b> er vores lille og hyggelige rollespilsCon, der består af rollespil, workshops, firgur spil, brætspil, god mad og massere af dårlig humor.</p>
    <p>Eventet foregår fra fredag til og med søndag sent i januar eller tidligt i februar.</p>

    <h4>Hvem er vi?</h4>
    <p>Vi er 5 seje kaniner i bestyrelsen, der står for at organisere eventet og sørger for, at der bliver bestilt en overflod af gulerødder og porrer</p>
    <p>men uden alle jer der møder op, laver mad, er spille mestre og gennerelt støtter op om vores lille con, ville vi ikke være hvor vi er nu <3</p>
    <p>Vi glæder os til at se jer igen, og igen, og igen...</p>

