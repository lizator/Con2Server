    <p>Det er ikke gratis at holde en Con i Danmark - eller andre steder. Måske? (Vi har faktisk ikke tjekket det - shhh).
        Pengene går udelukkende til at dække udgifter i forbindelse med foreningen Con2 - herunder lege af bil (så vi kan købe mad og slik),
        morgenmad, printerblæk, og en smule til fjollede tøjdyr.</p>

    <p><b>Bemærk:</b> Det koster 20 kr. ekstra at betale i døren.</p>

    <p>I år regner vi desværre ikke din pris automatisk ud. Du kan selv regne det ud lige nu, med priserne nedenfor -
        det er heldigvis ikke voldsomt kompliceret - eller du kan tilmelde dig og vente til vi sender dig en mail om
        hvor meget du skylder vores grådige leprechaun i banken. Hvis alt gå vel burde du have vores udregning af din
        tilmeldingspris inden for en uges tid, men det er en manuel process og kan derfor godt svinge lidt. Hvis det
        tager lidt lang tid eller vi er tæt på at Con2 går i luften må du meget gerne skrive til mail@con2.dk og prikke
        til vores regnskabsnisse. Husk at oplyse navn!</p>

